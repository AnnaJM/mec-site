            <h4 class="deptcont line">Aim</h4><br>
           <p>The department offers M Tech Programme in Mechanical Engineering with specialization in Energy Management. The objective of the course is to equip engineers to assess, preserve and manage energy resources in industries and other organizations. The course focuses on efficient utilization of thermal and non conventional energy by effective management and also to develop and propagate a culture of energy preservation among the students, engineers and industry.</p>
           <p>The mechanical engineering department is also engaged in the teaching of basic engineering subjects and provides courses Engineering Graphics, Engineering Mechanics, Basic Mechanical Engineering, Fluid dynamics, Strength of Materials, and Industrial Organization and Management. The focus of these basic courses is to create a set of engineers well versed in the basic engineering disciplines so as to solve real world problems accurately and efficiently. </p>

                   <ul>
                    <li>Subjects of industrial importance are Solar Energy Engineering, Process Reliability Engineering, Energy Conversion Systems.</li>
                    <li>    Solar Engineering lab and Computational lab are the labs set up under this department.
</li>
