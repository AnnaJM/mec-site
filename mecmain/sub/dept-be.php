            <h4 class="deptcont line">Aim</h4><br>
            <p>Biomedical Engineering is the application of engineering principles and design concepts to close the gap between engineering and medicine. It combines the design and problem solving skills of engineering with medical and biological sciences to advance healthcare treatment, including diagnosis, monitoring, treatment and therapy.</p>
            <p>Electronics and Biomedical Engineering of MEC was the first course in India to receive accreditation from the National Board of Accreditation (NBA). This course covers the basics required to help one get started in Biomedical Engineering. Over a period of 4 years, it covers a vast domain of areas in Electronics and Biomedical like Medical Physics, Signal Processing, Image Processing, Electronic Circuits, VLSI Design etc required in the Biomedical Field.</p>
            <p>The course deals with the detailed study of Electronics for application in the field of Biomedical Instrumentation. Labs  are  equipped  with  the  prominent diagnostic and therapeutic equipments. First in Kerala to start an engineering course in Electronics and Biomedical Engineering. Prides itself as being first in India to receive an accreditation for this B.Tech course.</p> 
            <p>The mission of the Biomedical Engineering program is to create, integrate, transfer and apply knowledge of engineering design and problem solving through the development of professionals and leaders by uniting engineering and health sciences faculty and counterparts in the community and create new medical devices, solve clinical problems, and promote collaboration with industry.</p>
            <h4 class="deptcont line">Objectives</h4><br>
            <ul>
              <li>To teach students how to analyse and implement practical solutions for complex interdisciplinary engineering problems.</li>
              <li>To give students a strong foundation for post graduate studies in the field of Electronics and Biomedical Engineering, Biometrics, Bioinformatics, and related disciplines.</li>
              <li>To teach students how to use state-of-the-art software tools to solve biomedical and electrical engineering problems.</li>
              <li>To provide students with hands-on engineering experience through laboratory courses and opportunities in industry and government laboratories.</li>
              <li>To cultivate the ability of the students to communicate and work effectively in teams.</li>
            </ul>
