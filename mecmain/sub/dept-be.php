            <h4 class="deptcont line">Recent Projects</h4><br>
            <ul style="list-style-type:none;">
              <li><span style="font-weight:400;">Neuro Linguistic Robot</span><br/> 
              The  project focuses on the movement of limbs  accompanied by event related re-synchronisation and synchronisation within the Electroencephalogram.The movement of the robot is controlled by a microprocessor according to the EEG signal.</li><br>
              <li><span style="font-weight:400;">Speech Trainer Kit Using Laryngeal Vibration</span><br/> 
              Aims  to  develop  hardware  and  software  tools  for  speech  training  for  hearing  impaired  on  a multilingual basis.  The  project consists of the development of a  software package for transforming speech signals into images where the laryngeal vibrations are used instead of speech signal.</li><br>
              <li><span style="font-weight:400;">Patient Monitoring System using GSM</span><br/> 
              The  project deals with wireless medical diagnostic system that can be portrayed as  a  model and helps  the  medical  practitioners  to  monitor  the  critical  parameters  of  a  patient  located  in  a geologically remote  location  with  the  help  of a  global  communication  medium,  that is  Global System for Mobile Communication (GSM).</li><br>
              <li><span style="font-weight:400;">Face as Mouse </span><br/> 
              This  technology  performs  hands free  cursor  control.  The  system  tracks  the movement of  face  to control the mouse  pointer. The  goal of the  project is  to create a  solution  that will  be usable for people with disabilities, with little to no cost involved.</li><br> 
              <li><span style="font-weight:400;">Low Cost Solution To Gait Cycle Digitization </span><br/>
              Analysis of human gait cycle using specially developed potentiometric sensors at the ankle, knee and hip to measure angular displacement. Gait analysis is a valuable tool in diagnosing walking disorders.</li><br>
              <li><span style="font-weight:400;">Volume Visualization in Medical Imaging </span><br/>
              It&#39;s a volume visualization system that provides a comprehensive 3-D image with several 2-D cross sectional slices given as inputs. The cross sectional images obtained from CT or MRI can be interpreted for diagnostic purposes.</li><br>
              <li><span style="font-weight:400;">Wavelet Analysis of ECG </span><br/>
              It provides a better description of EEG signals. In this system time frequency representation has been used. Wavelet transforms have time frequency capabilities. Further they provide multilead analysis of the signal.</li><br>
              <li><span style="font-weight:400;">REM &amp; Analysis of Chithra Valve </span><br/>
              Finite element analysis is a method for the analysis of stresses associated with complex structures. The project aims at modeling of heart valve and studying the stress distribution and displacement sensitivity due to loading.</li><br>
              <li><span style="font-weight:400;">Spectral Analysis of EEG Using FET </span><br/>
              This project aims at comparing the EEG of a normal person and that of an Alzheimer&#39;s patient from power spectrum obtained from digitized EEG wavelets. The decimation in time algorithm is used for extracting the frequency components from the EEG signals.</li><br>
              <li><span style="font-weight:400;">Non Linear Analysis of EEG Signal </span><br/>
              The aim of the project is to perform a spectral analysis of human EEG using wavelet transform. For this a multilevel wavelet transform analysis is performed. The signal is passed through a series of low pass and high pass filters and approximated detached coefficients obtained at each point are plotted.</li><br>
            </ul><br/>

